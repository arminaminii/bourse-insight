def seed():
    print("=" * 60)
    print("  Bourse Insight — Seed Data Script")
    print("=" * 60)

    # Create sectors
    sector_map = {}
    for name, name_en, code in SECTORS:
        sector, created = Sector.objects.get_or_create(
            code=code,
            defaults={'name': name, 'name_en': name_en, 'is_active': True},
        )
        sector_map[code] = sector
        status = "created" if created else "exists"
        print(f"  [{status}] Sector: {name} ({code})")

    # Create companies
    seen = set(Company.objects.values_list('symbol', flat=True))
    created_count = 0
    skipped = 0
    errors = []

    for symbol, name, sector_code, market in COMPANIES:
        if symbol in seen:
            skipped += 1
            continue
        try:
            Company.objects.create(
                symbol=symbol,
                name=name,
                sector=sector_map.get(sector_code),
                company_type=market,
                is_active=True,
            )
            seen.add(symbol)
            created_count += 1
        except Exception as e:
            errors.append(f"  ERROR: {symbol} — {e}")

    for err in errors:
        print(err)

    print(f"\n{'=' * 60}")
    print(f"  Sectors: {len(sector_map)}")
    print(f"  Companies created: {created_count}")
    print(f"  Skipped (duplicates): {skipped}")
    print(f"  Total in DB: {Company.objects.count()}")
    print(f"{'=' * 60}")


if __name__ == '__main__':
    seed()